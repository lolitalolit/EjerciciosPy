#Ejercicio 5: Crear un programa que pida al usuario ingresar 2 números por teclado y devuelva por pantalla la suma de ellos
# en un renglón, la resta en otro, el producto en otros y la división en otro.

primernum = int(input("Ingrese el primer numero: "))
segundonum = int(input("Ingrese el segundo numero: "))

print(primernum + segundonum)
print(primernum - segundonum)
print(primernum * segundonum)
print(primernum / segundonum)
